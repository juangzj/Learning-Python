# sum fucntion
def sum(a, b):
    return a + b


# Multiplication function
def multiplication(a, b):
    return a * b


# subtraction function
def subtraction(a, b):
    return a - b


# values to do a sum
print(f"The sum is:  {sum(5, 2)}")

# values to do a subtraction
print(f"The subtraction is: {subtraction(5,2)}")

# Values to do a multiplication
print(f"The multiplication is: {multiplication(2,3)}")
